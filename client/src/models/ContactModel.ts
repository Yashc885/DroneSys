import mongoose, { Schema } from 'mongoose';
const ContactSchema: Schema = new Schema({
  name: {
    type: String,
    required: true,
    trim: true
  },
  email: {
    type: String,
    required: true,
    trim: true,
    lowercase: true
  },
  message: {
    type: String,
    required: true,
    trim: true
  },
  timestamp: {
    type: Date,
    default: Date.now
  }
});

// Create and export the Contact model
const Contact = mongoose.model('Contact', ContactSchema);

export default Contact;
